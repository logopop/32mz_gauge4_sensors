void vPressure_Init(void)
{
   uint8_t                       ucBuff[250];                                   
   uint8_t i = 0x7f;
   

   memcpy(appData.uiI2CTxbuffer, "\xF4\x03", 2);  
   vI2C_Write(DEV_PRESS, 2);
   vLongDelay(100);

}

//*********************************************************************************************************************
// 
// 
//*********************************************************************************************************************
//

void vPressure_State(void)
{      
   static unsigned char          i                          = 0;
     
	switch (appData.uiGyrostate)
   {
      case 0:        // Start
         vPressure_Init();
         appData.uiPressurestate = 1;   
      break;
      case 1:        // Wait
         if (APP_Check_Transfer_Status(appData.drvI2CHandle1, appData.appI2CWriteBufferHandle) == DRV_I2C_BUFFER_EVENT_COMPLETE)
            appData.uiPressurestate = 2;  
      break;
      case 2:
         // Done
         
      break;
      
	
	}
	
	
	
	 
}

