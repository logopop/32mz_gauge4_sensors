int vDisplay_State()
{      
   unsigned char                 ucBuff[]                   = {"Hello World!\0"};
   static unsigned char          i                          = 0;
   unsigned char                 ucTemp[20]                 = {0}; 
     
	switch (appData.uiDisplayState)
   {
      case 0:        // Start and clear
         by1306_Clear();   
         appData.uiDisplayDelay = 0;
         appData.uiDisplayState = 2;
         
      break;
      case 2:        // Print logo
         by1306_Reset();
         I2C_WriteDataStream(bLogo, sizeof(bLogo));
         appData.uiLogoTime = appData.ulLife;
         appData.uiDisplayState = 3;                        
      break;
      case 3:
         appData.uiDisplayState = 4;         
         
      break;   
      case 4:
         if ((appData.ulLife - appData.uiLogoTime) > 2000)     // 2 seconds
         {
            appData.uiDisplayState = 5;         
            by1306_Clear();
         }   
      break;   
      case 5:
         appData.uiDisplayState = 6;         
         if (memcmp(appData.uiDebugbuffer0, appData.uiPrevbuffer0, 16))
         {        
            by1306_DrawText(0, 0, appData.uiDebugbuffer0, FONT_VGA);
            memcpy(appData.uiPrevbuffer0, appData.uiDebugbuffer0, 16);
         }   
      break;   
      case 6:
         appData.uiDisplayState = 7;                  
         if (memcmp(appData.uiDebugbuffer1, appData.uiPrevbuffer1, 16))
         {        
            by1306_DrawText(0, 1, appData.uiDebugbuffer1, FONT_VGA);
            memcpy(appData.uiPrevbuffer1, appData.uiDebugbuffer1, 16);
         }   
      break;            
      case 7:        // Print logo
         appData.uiDisplayState = 8;         
         if (memcmp(appData.uiDebugbuffer2, appData.uiPrevbuffer2, 16))
         {        
            by1306_DrawText(0, 2, appData.uiDebugbuffer2, FONT_VGA);
            memcpy(appData.uiPrevbuffer2, appData.uiDebugbuffer2, 16);
         }   
      break;
      case 8:      // Dump debug buffers forever    
         appData.uiDisplayState = 9;
         if (memcmp(appData.uiDebugbuffer3, appData.uiPrevbuffer3, 16))
         {        
            by1306_DrawText(0, 3, appData.uiDebugbuffer3, FONT_VGA);
            memcpy(appData.uiPrevbuffer3, appData.uiDebugbuffer3, 16);
         }   
      break;   
      case 9:         
         appData.uiDisplayState = 10;         
         if (memcmp(appData.uiDebugbuffer4, appData.uiPrevbuffer4, 16))
         {        
            by1306_DrawText(0, 4, appData.uiDebugbuffer4, FONT_VGA);
            memcpy(appData.uiPrevbuffer4, appData.uiDebugbuffer4, 16);
         }   
      break;
      case 10:         
         appData.uiDisplayState = 11;         
         if (memcmp(appData.uiDebugbuffer5, appData.uiPrevbuffer5, 16))
         {        
            by1306_DrawText(0, 5, appData.uiDebugbuffer5, FONT_VGA);
            memcpy(appData.uiPrevbuffer5, appData.uiDebugbuffer5, 16);
         }   
      break;
      case 11:         
         appData.uiDisplayState = 12;         
         if (memcmp(appData.uiDebugbuffer6, appData.uiPrevbuffer6, 16))
         {        
            by1306_DrawText(0, 6, appData.uiDebugbuffer6, FONT_VGA);
            memcpy(appData.uiPrevbuffer6, appData.uiDebugbuffer6, 16);
         }   
      break;
      case 12:       
         appData.uiDisplayState = 100;         
         if (memcmp(appData.uiDebugbuffer7, appData.uiPrevbuffer7, 16))
         {        
            by1306_DrawText(0, 7, appData.uiDebugbuffer7, FONT_VGA);
            memcpy(appData.uiPrevbuffer7, appData.uiDebugbuffer7, 16);
         }   
      break; 
      
      case 100:
         // Done
         
         if (!appData.ucDisplayPresent)
         {
            appData.ucDisplayPresent = 1;
            by1306_Initialize();  
            appData.uiDisplayState = 0;
         }
         else
            appData.uiDisplayState = 5; 
      break;      	
	}	     	    
   return 0;
}

